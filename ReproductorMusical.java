    private void botonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgregarActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);

        int resultado = fileChooser.showOpenDialog(this);

        if (resultado == JFileChooser.APPROVE_OPTION) {
            // Obtiene la carpeta seleccionada
            File carpeta = fileChooser.getSelectedFile();

            // Filtra los archivos MP3 en la carpeta
            File[] archivosMP3 = carpeta.listFiles(new FilenameFilter() {
                @Override
                public boolean accept(File dir, String name) {
                    return name.toLowerCase().endsWith(".mp3");
                }
            });

            for (File archivo : archivosMP3) {
                String nombreCancion = archivo.getName();
                listaCanciones.add(nombreCancion);
            }
           // aqui se van actualizando la lista de las canciones.
            actualizarListaCanciones();
        }
    }//GEN-LAST:event_botonAgregarActionPerformed

