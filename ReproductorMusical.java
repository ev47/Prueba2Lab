    private void actualizarListaCanciones() {
        // Puedes mostrar la lista de canciones en un JTextArea u otro componente
        jTextArea1.setText("Lista de Canciones:\n");
        for (String cancion : listaCanciones) {
            jTextArea1.append(cancion + "\n");
        }
    
    }

